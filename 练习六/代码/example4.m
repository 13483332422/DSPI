% 一数字滤波器采样频率fs = 1kHz，要求滤除100Hz的干扰，
% 其３dB的边界频率为95Hz和105Hz，原型归一化低通滤波器为 
w1=95/500; %归一化边界频率
w2=105/500; 
[B,A]=butter(1,[w1, w2],'stop'); %参数's'缺省是数字滤波器
[h,w]=freqz(B,A); %返回频率响应(响应是同一个响应,x周不同),和数字频率
f=w/pi*500; %计算模拟频率
plot(f,20*log10(abs(h))); 
axis([50,150,-30,10]); 
grid; 
xlabel('频率/Hz') 
ylabel('幅度/dB')
